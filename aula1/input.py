# para receber input externo, se usa a função 'input', dentro dela vai o texto que deve ser exibido na pergunta
nome = input("Entre com seu nome:")

# o input é salvo como texto na variável após apertar o Enter no teclado
idade = input("Entre com sua idade:")

altura = input("Entre com sua altura:")

print(f"Seu nome é {nome}, você tem {idade} anos e {altura} de altura")
