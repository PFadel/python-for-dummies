# toda variável tem um nome
# esse nome precisa começar com uma letra ou 'underline' _ a letra pode ser maiúscula ou minúscula
# as letras não podem ter acento
# ele pode ter números depois da primeira letra
# ele pode ter 'underline' _ depois da primeira letra
# outros caracteres especiais não são permitidos, @ , # , % e etc.

# variáveis do tipo texto, ou 'string'
nome = "Pedro"
sobrenome = "Fadel"

# variáveis com números inteiros, ou 'int'
idade = 27

# variáveis com números reais, ou 'float'
altura = 1.68

# variáveis com valor verdadeiro e falso, ou 'boolean' / 'bool'
ja_teve_cabelo_azul = True
tem_casa_propria = False

print("Olá, tudo bem ?")
# usando 'f' antes do texto do print, é possível colocar variáveis dentro do texto
print(f"Meu nome é {nome}, tenho {idade} anos e {altura} de altura")
